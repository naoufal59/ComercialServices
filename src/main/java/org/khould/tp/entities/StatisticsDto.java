package org.khould.tp.entities;

public class StatisticsDto {
	public Long entrpriseview;
	public Long numberOfClients;
	public Long visitsCount;
	public Long getEntrpriseview() {
		return entrpriseview;
	}
	public void setEntrpriseview(Long announcesViews) {
		this.entrpriseview = announcesViews;
	}
	public Long getNumberOfClients() {
		return numberOfClients;
	}
	public void setNumberOfClients(Long numberOfClients) {
		this.numberOfClients = numberOfClients;
	}
	public Long getVisitsCount() {
		return visitsCount;
	}
	public void setVisitsCount(Long visitsCount) {
		this.visitsCount = visitsCount;
	}
}
